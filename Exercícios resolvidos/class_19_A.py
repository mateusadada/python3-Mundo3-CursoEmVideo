# Dicionários são encontrados por índices literais

# cria
dados = {'nome': 'Pedro', 'idade': 25}

# exibe por índice
print(dados['nome'])
print(dados['idade'])

# adiciona e exibe
dados['sexo'] = 'M'
print(dados['sexo'])

# apaga um índice
del dados['idade']

# exibe tudo
print(dados)
